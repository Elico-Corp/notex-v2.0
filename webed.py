#!bin/python

###############################################################################
###############################################################################

from webed.app import app
from webed.ext import db

from flask.ext.script import Manager

###############################################################################
###############################################################################

manager = Manager (app)

###############################################################################
###############################################################################

@manager.command
def run (debug=False):
    """Runs the Flask server i.e. app.run (debug=True|False)"""
    app.run (debug=debug)

@manager.command
def init ():
    """Creates & initializes database and tables"""
    db.create_all ()

@manager.command
def drop ():
    """Drops all database tables"""
    db.drop_all ()

###############################################################################
###############################################################################

if __name__ == '__main__':

    manager.run ()

