from flask.ext.admin import Admin, AdminIndexView, expose
from ..app import app

###############################################################################
###############################################################################

class IndexView (AdminIndexView):

    @expose ('/')
    def index (self):

        return self.render ('admin.html', debug=app.debug)

###############################################################################
###############################################################################

admin = Admin (app, name='WebEd', index_view=IndexView (name='Home'))

