Ext.define ('Webed.controller.window.ConfirmBox', {
    extend: 'Ext.app.Controller',

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////

    refs: [{
        selector: 'confirm-box', ref: 'confirmBox'
    }],

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////

    init: function () {
        this.control ({
            'confirm-box button[action=confirm]': {click: this.confirm},
            'confirm-box button[action=cancel]': {click: this.cancel}
        });
    },

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////

    confirm: function () {
        var box = assert (this.getConfirmBox ());
        box.fireEvent ('confirm', box);
    },

    cancel: function () {
        var box = assert (this.getConfirmBox ());
        box.fireEvent ('cancel', box);
    }

    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
});
