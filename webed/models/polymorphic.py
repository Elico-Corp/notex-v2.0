from sqlalchemy.ext.declarative import declared_attr
from ..ext.db import db

###############################################################################
###############################################################################

class Polymorphic (object):

    type = db.Column ('type', db.String (24), nullable=False, index=False)

    @declared_attr
    def __mapper_args__ (cls):

        return {
            'polymorphic_identity': cls.__name__, 'polymorphic_on': cls.type
        }

